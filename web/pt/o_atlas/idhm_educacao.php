        <div id="text_met">
	<span><b>O que compõe o IDHM Educação</b></span><br />
	O IDHM Educação é uma composição de indicadores de escolaridade da população adulta e de fluxo escolar da população jovem. 
        </div>
        <div class="clear"></div>
        <img src="./img/o_atlas/o_que_merece.jpg" id="dim_saud_images">
        <div id="text_met">
	<span><b>O que medem esses indicadores?</b></span><br />
	A <b>escolaridade da população adulta</b> é medida pelo percentual de pessoas de 18 anos ou mais de idade com o ensino fundamental completo.<br /><br /> 
            O <b>fluxo escolar da população jovem</b> é medido pela média aritmética do percentual de crianças de 5 a 6 anos frequentando a escola, do percentual de jovens de 11 a 13 anos frequentando os anos finais do ensino fundamental, do percentual de jovens de 15 a 17 anos com ensino fundamental completo e do percentual de jovens de 18 a 20 anos com ensino médio completo. 
        </div>
        <div class="clear"></div>
        <img src="./img/o_atlas/porque_esse_indicador.jpg" id="dim_saud_images">
        <div id="text_met">
	<span><b>Por que estes indicadores?</b></span><br />
	A <b>escolaridade da população adulta</b> reflete o funcionamento do sistema educacional em períodos passados e considera que a população adulta brasileira deveria ter completado, ao menos, o ensino fundamental em sua passagem pelo sistema educacional.<br /><br />
            O <b>fluxo escolar da população jovem</b> acompanha a população em idade escolar em quatro momentos importantes da sua formação: entrada no sistema educacional, finalização do primeiro ciclo do ensino fundamental e conclusão do ensino fundamental e do ensino médio. Esta composição envia uma mensagem importante: de que as crianças, ao menos a partir dos 5 anos de idade, precisam já estar na escola; que as crianças de 12 anos precisam estar nos anos finais do ensino fundamental; que os jovens de 16 anos precisam ter concluído o ensino fundamental; e que os jovens de 19 anos precisam ter concluído o ensino médio. A expansão dessas faixas etárias no cálculo do indicador se dá por questões amostrais e estatísticas.
        </div>
        <div class="clear"></div>
        <img src="./img/o_atlas/como_e_calculado.jpg" id="dim_saud_images">
        <div id="text_met">
	<span><b>Como são calculados estes indicadores?</b></span><br />
	Os indicadores são obtidos a partir das respostas ao questionário da amostra do Censo Demográfico. 
        </div>
        <div class="clear"></div>
