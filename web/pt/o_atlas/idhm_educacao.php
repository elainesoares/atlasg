	<span><b>Comparação e ajustes a partir do IDH Global</b></span><br />
	Assim como o IDH Global, o IDHM Educação é composto por um indicador que fornece informação sobre a situação educacional da população adulta e um referente à população em idade escolar. Entretanto, as variáveis são outras.
            <p>No caso da população adulta, não há no Censo Demográfico de 2010 a informação da média de anos de estudo de pessoas de 25 anos ou mais para o nível municipal, tal como é medido no IDH Global. Assim, foi feita uma adaptação da variável, adotando-se a proporção da população de 18 anos ou mais que concluiu o ensino fundamental.</p>
            <p>No caso da população jovem, a metodologia aplicada pelo IDH Global a partir de 2010 – a expectativa de vida escolar – é uma medida de retenção das crianças na escola, independentemente da repetência, o que não se aplica para o caso brasileiro, já que o desafio atual da educação no país é a qualidade e o fluxo escolar, mais que a entrada e a permanência no sistema. Também inclui educação superior, o que não se aplica aos municípios brasileiros. </p>
        </div>
