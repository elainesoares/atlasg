	<span><b>Indicadores de suporte à análise do IDHM Educação</b></span><br />
	Além dos 5 indicadores que compõem o IDHM Educação, o Atlas 2013 apresenta inúmeros outros dados que permitem uma visão mais completa e detalhada da situação do município nesta dimensão, abordando, para faixas etárias diferenciadas, os temas analfabetismo, grau de escolaridade (fundamental completo, médio completo, superior completo) e frequência escolar (frequência bruta, líquida, atraso escolar). O Atlas 2013 também apresenta a variável Anos Esperados de Estudo, que é uma adaptação metodológica da métrica usada no IDH Global, mas considerando 12 anos como máximo de anos de estudos da educação formal e ajustando estes valores para repetência.
        </div>
