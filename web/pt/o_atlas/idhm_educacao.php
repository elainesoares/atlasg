	<span><b>Como é o cálculo do IDHM Educação?</b></span><br />
	Considerando-se que as taxas de frequência e de conclusão  variam entre 0% e 100% e que os valores mínimo e máximo escolhidos são também 0% e 100%, para "convertê-las" em um índice variando de 0 a 1, basta dividir a taxa por 100. <br /><br />
            Assim, se um município tem:<br />
            - 65% de sua população adulta (18 anos ou mais) com ensino fundamental completo<br />
            - 85% de crianças de 5 a 6 anos na escola<br />
            - 80% de crianças de 11 a 13 nos anos finais do ensino fundamental<br />
            - 70% de crianças de 15 a 17 anos com ensino fundamental completo<br />
            - 50% de jovens de 18 a 20 anos com ensino médio completo<br /><br />
            <p>Seu <b>índice de escolaridade da população adulta</b> será 0,650.</p>
            <p>Seu <b>índice de fluxo escolar da população jovem</b> será a média aritmética dos subíndices referentes aos 4 indicadores, com peso igual  = (0,850+0,800+0,700+0,500) /4 = 0,712</p>
