        <div id="text_met">
	<span><b>Limitações</b></span><br />
	A medida da educação da população jovem não inclui toda a população em idade escolar e frequentando a escola, captando apenas determinados momentos da passagem da população jovem pelo sistema educacional. Também inclui, por questões amostrais e estatísticas, faixas etárias ampliadas daquela faixa etária ideal: 12 anos nos anos finais do fundamental, 16 anos com ensino fundamental completo, e 19 anos com ensino médio completo. 
            <p>A medida da educação da população adulta limita a avaliação desta população àqueles que completaram o ensino fundamental, não incluindo aqueles que tiveram alguma passagem pelo sistema educacional sem completar ciclos. Também pressupõe apenas ensino fundamental como teto para o acesso pela  a conhecimento pela população adulta, no contexto histórico nacional.</p>
        </div>
