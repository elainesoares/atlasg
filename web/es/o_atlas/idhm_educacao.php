	<span><b>Comparación y ajustes a partir del IDH Global</b></span><br />
	Al igual que el IDH Global, el IDHM Educación comprende un indicador que proporciona información sobre la situación educacional de la población adulta y sobre la población en edad escolar. Sin embargo, se usaron otras variables.
            <p>En el caso de la población adulta, en el censo demográfico de 2010 no hay información sobre el número promedio de años de estudio de personas de al menos 25 años a nivel municipal tal como se mide en el IDH Global. Por lo tanto, se adaptó la variable usando la proporción de la población de 18 años o más que concluyó la educación primaria.</p>
            <p>En el caso de la población joven, la metodología adoptada por el IDH Global a partir de 2010 —la expectativa de vida escolar— evalúa el nivel de retención de los niños en la escuela, independientemente de sus repeticiones, lo que no rige para el caso de Brasil, donde el desafío actual de la educación es más bien la calidad y el flujo escolar, más que la entrada y permanencia en el sistema. La metodología incluye asimismo la educación superior, que tampoco se aplica a los municipios brasileños.</p>
        </div>
