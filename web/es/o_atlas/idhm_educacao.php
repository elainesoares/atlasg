        <div class="block-01-div-subtitle" style="margin-top: -17px; font-size: 23pt; margin-bottom: -13px;">Acceso al conocimiento</div>
        <p>En el IDHM, esta dimensión se mide en función del nivel de escolaridad de la población adulta y del flujo escolar de la población joven.</p>
    </div>
