    <div id="dim_saud_direita" style='height: 2000px;'>
        <img src="./img/o_atlas/o_que_componhe.jpg" id="dim_saud_images">
        <div id="text_met">
	<span><b>¿De qué se compone el IDHM Educación?</b></span><br />
	El IDHM Educación es una combinación de indicadores de escolaridad de la población adulta y del flujo escolar de la población joven. 
        </div>
        <div class="clear"></div>
        <img src="./img/o_atlas/o_que_merece.jpg" id="dim_saud_images">
        <div id="text_met">
	<span><b>¿Qué miden esos indicadores?</b></span><br />
	La <b>escolaridad de la población adulta</b> se mide de acuerdo con el porcentaje de personas de 18 años o más que hayan cursado la educación secundaria completa.<br /><br /> 
            El <b>flujo escolar de la población joven</b> se mide calculando el promedio aritmético del porcentaje de niños de 5 a 6 años que van a la escuela, del porcentaje de jóvenes de 11 a 13 años que se encuentran en los últimos años de la educación primaria, del porcentaje de jóvenes de 15 a 17 años que han terminado la educación primaria y del porcentaje de jóvenes de 18 a 20 años que han terminado la enseñanza secundaria. 
        </div>
        <div class="clear"></div>
        <img src="./img/o_atlas/porque_esse_indicador.jpg" id="dim_saud_images">
        <div id="text_met">
	<span><b>¿Por qué estos indicadores?</b></span><br />
	La <b>escolaridad de la población adulta</b> refleja el funcionamiento del sistema educativo en períodos pasados y considera que la población adulta brasileña debe haber terminado al menos la educación primaria.<br /><br />
            El <b>flujo escolar de la población joven</b> hace un seguimiento de la población en edad escolar en cuatro momentos importantes de su formación: la entrada al sistema educativo, el término del primer ciclo de la educación primaria, el término de la educación primaria y el término de la educación secundaria. Esta composición expresa un mensaje importante: de que los niños, al menos a partir de los 5 años, ya deben estar acudiendo a la escuela, de que los niños de 12 años deben encontrarse en los últimos años de la educación primaria, de que los jóvenes de 16 años tienen que haber terminado la educación primaria y de que los jóvenes de 19 años tienen que haber terminado la educación secundaria. La ampliación de estos rangos de edad para calcular el indicador se debe a razones estadísticas y de muestreo.
        </div>
        <div class="clear"></div>
        <img src="./img/o_atlas/como_e_calculado.jpg" id="dim_saud_images">
        <div id="text_met">
	<span><b>¿Cómo se calculan estos indicadores?</b></span><br />
	Los indicadores se obtienen a partir de las respuestas proporcionadas en el cuestionario del censo demográfico.
        </div>
        <div class="clear"></div>
        <img src="./img/o_atlas/limitacoe.jpg" id="dim_saud_images">
        <div id="text_met">
	<span><b>Limitaciones</b></span><br />
	La medición del nivel de educación de la población joven no abarca toda la población en edad escolar y que va a la escuela, sino que capta apenas determinados momentos del paso de la población joven por el sistema educativo. El indicador también abarca, por razones estadísticas y de muestreo, rangos de edad más amplios que los rangos ideales: 12 años al final de la educación primaria, 16 años al término de la educación primaria y 19 años con la educación secundaria completa. 
            <p>La medición del nivel de educación de la población adulta se limita a aquellas personas que terminaron la educación primaria y no toma en cuenta a aquellas que pasaron por el sistema educativo sin concluir un ciclo. Asimismo, considera apenas la educación primaria como techo para el acceso de la población adulta al conocimiento en el contexto histórico nacional.</p>
        </div>
        <div class="clear"></div>
