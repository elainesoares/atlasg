        <div id="text_met">
	<span><b>¿Cómo se calcula el IDHM Educación?</b></span><br />
	Considerando que los índices de asistencia y de conclusión varían entre un 0 % y un 100 % y que los valores mínimo y máximo elegidos también son 0 % y 100 %, para convertirlos en un índice de 0 a 1 basta dividirlos por 100. <br /><br />
            Así, cuando un municipio tiene<br />
            - el 65% de la población adulta (18 años o más) con educación primaria completa<br />
            - el 85% de los niños de 5 y 6 años matriculados en una escuela<br />
            - el 80% de los niños de 11 a 13 años cursando los últimos años de la educación primaria<br />
            - el 70% de los niños de 15 a 17 con educación primaria completa<br />
            - y el 50% de los jóvenes de 18 a 20 años con educación secundaria completa<br /><br />
            <p>Su <b>índice de escolaridad de la población adulta es de</b> 0,650.</p>
            <p>En tanto, el <b>índice de flujo escolar de la población joven</b> equivale al promedio aritmético de los subíndices referentes a los cuatro indicadores, con igual coeficiente: (0,850 + 0,800 + 0,700 + 0,500) /4 = 0,712</p>
