	<span><b>Indicadores de apoyo para el análisis del IDHM Educación</b></span><br />
	Además de los 5 indicadores que componen el IDHM Educación, el Atlas 2013 presenta un sin número de datos adicionales que brindan una visión más completa y detallada de la situación de cada municipio en este ámbito al abordar, según rangos de edad diferenciados, aspectos como el analfabetismo, el nivel de educación (primaria completa, secundaria completa y superior completa) y la asistencia escolar (asistencia bruta y neta y retraso escolar). El Atlas 2013 también presenta la variable años de estudio esperados, una adaptación metodológica de la métrica usada en el IDH Global pero considerando un período máximo de estudios de 12 años en la educación formal y adaptando estos valores a las repeticiones.
        </div>
