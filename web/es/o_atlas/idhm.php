    <div id="texto_idhm">
        <p>En 2012, el PNUD Brasil, el IPEA y la Fundación João Pinheiro asumieron el desafío de adaptar la metodología del IDH Global para calcular el IDH municipal (IDHM) de los 5565 municipios brasileños usando los datos del censo demográfico de 2010. Asimismo, se recalculó el IDHM, usando la nueva metodología, para los años 1991 y 2000 mediante una minuciosa adaptación de las diferentes áreas municipales con el fin de tomar en cuenta las divisiones administrativas realizadas entre 1991, 2000 y 2010 y permitir la comparabilidad temporal y espacial de los municipios. Ya se había enfrentado este desafío en las ediciones de 1998 y 2003 del Atlas.<p/>
        <p>El IDHM brasileño se basa en las mismas tres dimensiones del IDH Global (longevidad, educación e ingresos), pero va más allá, pues adapta la metodología al contexto brasileño y a la disponibilidad de indicadores nacionales. Aunque midan los mismos fenómenos, los indicadores abarcados por el IDHM son más adecuados para evaluar el desarrollo de los municipios brasileños.</p> 
        <p>Así, el IDHM, incluidos sus tres componentes —el IDHM Longevidad, el IDHM Educación y el IDHM Ingreso—, nos cuenta un poco de la historia de los municipios en función de tres aspectos importantes de desarrollo humano en el transcurso de dos decenios.</p>
        <p>El IDHM está acompañado de más de 200 indicadores socioeconómicos que sustentan el análisis del IDHM y profundizan la comprensión de los fenómenos y dinámicas del desarrollo municipal. El IDHM y los indicadores de apoyo están consignados en el Atlas de Desarrollo Humano en Brasil 2013.</p>
