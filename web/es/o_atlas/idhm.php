    <div id="calculo_idhm">
        <div id="text_calculo_idhm">
            <p>El objetivo a la hora de elaborar la metodología de cálculo del IDHM fue adecuar la metodología del IDH Global para:</p><br />
            <div id="a_b" style='margin-right: 30px;'>
                <b><font color="#BDBDBD" size="5">a)</font></b><br />
                    Adaptar la metodología al contexto brasileño, buscando indicadores más adecuados para evaluar las condiciones de núcleos sociales más acotados — los municipios. 
            </div>
            <div id="a_b">
                <b><font color="#BDBDBD" size="5">b)</font></b><br />
                Adaptar la metodología del IDH Global a los indicadores disponibles en los censos demográficos brasileños, con el fin de garantizar el uso de una misma fuente de datos y la comparabilidad de todos los municipios.
            </div>
            <div class="clear"></div>
            
            <br />
            <p><b>La vida larga y saludable </b> se mide en función de la expectativa de vida al nacer, usando un método indirecto a partir de los datos de los censos demográficos del IBGE. 
                    Este indicador muestra el número promedio de años que viviría una persona en un determinado municipio a partir del nacimiento si se mantienen los mismos índices de mortalidad.
            </p>
            
            <br />
            <p><b>El acceso al conocimiento  </b>se mide a partir de dos indicadores. La escolaridad de la población adulta se mide de acuerdo con el porcentaje de personas de 18 años o más que hayan cursado la enseñanza primaria completa; este indicador tiene un coeficiente 1.  El flujo escolar de la población joven se mide calculando el promedio aritmético del porcentaje de niños de 5 a 6 años que van a la escuela, del porcentaje de jóvenes de 11 a 13 años que se encuentran en los últimos años de la enseñanza primaria, del porcentaje de jóvenes de 15 a 17 años que han terminado la enseñanza primaria y del porcentaje de jóvenes de 18 a 20 años que han terminado la educación secundaria; este indicador tiene un coeficiente 2. Esta medida hace un seguimiento de la población en edad de escolarización en cuatro momentos importantes de su formación, lo que ayuda a los gestores a determinar si los niños y jóvenes están cursando los ciclos educativos adecuados para su edad.  El IDHM Educación es el promedio geométrico de estos dos componentes. Los datos usados son los del censo demográfico del IBGE. 
            </p>
            
            <br />
            <p><b>El estándar de vida  </b> se mide en función del ingreso municipal per cápita, a saber, los ingresos promedio de los habitantes de un municipio determinado. Es la suma de los ingresos de todos los habitantes dividida por el número de personas que viven en el municipio, incluidos los niños y las personas que no acreditan ingresos.  Los datos usados son los de los censos demográficos del IBGE.</p>
