            <p><b>Gestores estaduales y federales</b> El Atlas permite a los gestores de políticas federales y estaduales identificar con precisión las regiones que requieren intervención mediante programas, políticas sectoriales y territoriales y medidas específicas.</p>
            <p><b>Actores municipales</b> El Atlas proporciona apoyo tanto a la administración pública como a la sociedad civil local para establecer diagnósticos de los principales desafíos que enfrentan los municipios proporcionando a los gestores herramientas pertinentes y ampliando el espacio de participación de la sociedad. </p>
            <p><b>Investigadores</b> El análisis comparativo de los datos municipales y su evolución en el tiempo mediante indicadores seleccionados ayuda a los investigadores a estudiar el impacto de las políticas públicas al permitirles identificar los programas y experiencias exitosas y reproducibles y mapear los desafíos y oportunidades de cada territorio.</p>
            <p><b>Sociedad civil y sector privado</b> El Atlas orienta a la sociedad civil y al sector privado en sus iniciativas de desarrollo nacional, incluidas la asignación de recursos y la definición del público beneficiario.</p>
            <p><b>Ciudadanos</b> El acceso a informaciones claras y concisas fomenta la participación social y ayuda a los ciudadanos brasileños a ser cada vez más protagonistas de su realidad y su futuro.</p>
