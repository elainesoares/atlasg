    <div id="texto_paraque">
        <p>
            El objetivo del Atlas Brasil 2013 es <b>brindar una herramienta a la sociedad</b>. 
            La democratización de la información a nivel municipal contribuye al 
            <b>fortalecimiento de las capacidades locales, a la mejora de la gestión 
            pública y al empoderamiento de los ciudadanos</b> brasileños mediante la 
            ampliación de los conocimientos sobre su propia realidad.
        </p>
        
        <p>
            De esa forma, el Atlas fomenta un <b>diálogo informado y bien fundado 
            sobre desarrollo</b> a partir de un parámetro usado a nivel internacional: 
            el Índice de Desarrollo Humano. Es una herramienta que <b>estimula el uso 
            de datos socioeconómicos</b> para estudiar nuestra sociedad.
        </p>
        
        <p>
            El Atlas confiere <b>transparencia a los procesos de desarrollo</b> en importantes 
            ámbitos sociales. El retrato proporcionado por la herramienta ayuda 
            a hacer un <b>seguimiento de los avances logrados</b> por los municipios 
            brasileños durante los últimos veinte años y permite producir análisis 
            que contribuyan a trazar los caminos del futuro.
        </p>

