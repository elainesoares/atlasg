<div class="areatitle">Glosario</div>
<div class="block-01-div-subtitle">DEMOGRAFÍA Y SALUD<br /><br /></div>
	<div id="text_met">
		<span><b>Esperanza de vida al nacer</b></span><br>
		Número promedio de años que deberían de vivir las personas a partir del nacimiento si se mantuvieran constantes a lo largo de su vida los niveles y el patrón de mortalidad por edad observados en el año del censo.  
    </div>
	
	<div id="text_met">
		<span><b>Tasa de fecundidad total</b></span><br>
		Número promedio de hijos que deberá tener una mujer al concluir el período reproductivo (15 a 49 años). 
    </div>
	
	<div id="text_met">
		<span><b>Mortalidad infantil</b></span><br>
		Número de niños que no deben sobrevivir al primer año de vida de cada 1000 niños nacidos vivos. 
    </div>
	
	<div id="text_met">
		<span><b>Mortalidad hasta los 5 años</b></span><br>
		Probabilidad de fallecer entre el nacimiento y a la edad exacta de 5 años por cada 1000 niños nacidos vivos. 
    </div>
	
	<div id="text_met">
		<span><b>Probabilidad de supervivencia hasta los 40 años</b></span><br>
		Probabilidad de que un niño recién nacido viva hasta los 40 años si se mantienen constantes a lo largo de su vida el nivel y el patrón de mortalidad por edad en el año del censo. 
    </div>
	
	<div id="text_met">
		<span><b>Porcentaje de personas que viven en hogares con razón de dependencia > 75%</b></span><br>
		Razón entre las personas que viven en hogares donde la razón de dependencia es superior a un 75 % y el total de personas que viven en domicilios particulares permanentes multiplicada por 100. La razón de dependencia es la razón entre el número de personas de hasta 14 años y de al menos 65 años (población dependiente) y el número de personas de entre 15 y 64 años (población potencialmente activa) multiplicada por 100. 
    </div>
	
	<div id="text_met">
		<span><b>Tasa de envejecimiento</b></span><br>
		Razón entre la población de al menos 65 años y la población total multiplicada por 100.
    </div>


<div class="block-01-div-subtitle">EDUCACIÓN<br /><br /></div>
	<div id="text_met">
		<span><b>Expectativa de años de estudio</b></span><br>
		Número promedio de años de estudio que una generación de niños que se matricula en la escuela deberá completar al alcanzar a los 18 años de edad si se mantienen los patrones actuales a lo largo de su vida escolar. 
    </div>
	
	<div id="text_met">
		<span><b>Tasa de analfabetismo de la población de 11 a 14 años de edad</b></span><br>
		Razón entre la población de 11 a 14 años de edad que no sabe leer ni escribir una nota simple y el total de personas en este rango de edad multiplicada por 100.
    </div>
	
	<div id="text_met">
		<span><b>Tasa de asistencia bruta a la educación primaria y secundaria</b></span><br>
		Razón entre el número total de personas de cualquier edad que cursa la enseñanza primaria o secundaria y la población de entre 6 y 17 años multiplicada por 100. 
    </div>
	
	<div id="text_met">
		<span><b>Tasa de asistencia neta a la educación primaria y secundaria</b></span><br>
		Razón entre el número total de personas de entre 6 y 17 años que cursa la enseñanza primaria o secundaria y la población total en ese mismo rango de edad multiplicada por 100.
    </div>

<div class="block-01-div-subtitle">INGRESOS<br /><br /></div>
	<div id="text_met">
		<span><b>Ingreso per cápita</b></span><br>
		Razón entre la suma de los ingresos de todos los individuos residentes en domicilios particulares permanentes y el número total de dichos individuos. Valores expresados en reales del 1 de agosto de 2010. 
    </div>
	
	<div id="text_met">
		<span><b>Proporción de personas extremadamente pobres</b></span><br>
		Proporción de individuos con ingreso familiar per cápita igual o inferior a 70,00 R$ mensuales, expresada en reales de agosto de 2010. El universo de individuos se limita a las personas que viven en domicilios particulares permanentes. 
    </div>
	
	<div id="text_met">
		<span><b>Proporción de pobres</b></span><br>
		Proporción de individuos con ingreso familiar per cápita igual o inferior a 140,00 R$ mensuales, expresada en reales de agosto de 2010. El universo de individuos se limita a las personas que viven en domicilios particulares permanentes. 
    </div>
	
	<div id="text_met">
		<span><b>Proporción de personas vulnerables a la pobreza</b></span><br>
		Proporción de individuos con ingreso familiar per cápita igual o inferior a 255,00 R$ mensuales, expresada en reales de agosto de 2010, equivalente a la mitad de un sueldo mínimo en ese entonces. El universo de individuos se limita a las personas que viven en domicilios particulares permanentes. 
    </div>
	
	<div id="text_met">
		<span><b>Razón entre el 20 % de los más ricos y el 40 % de los más pobres</b></span><br>
		Medida del nivel de desigualdad existente en la distribución de individuos según el ingreso familiar per cápita. Esta medida compara los ingresos per cápita de los individuos pertenecientes al quintil más rico de esa distribución con los ingresos per cápita promedio de los individuos de los dos quintiles más pobres. El universo de individuos se limita a las personas que viven en domicilios particulares permanentes. 
    </div>
	
	<div id="text_met">
		<span><b>Índice de Theil - L</b></span><br>
		Mide la desigualdad en la distribución de individuos según el ingreso familiar per cápita, sin considerar aquellos que presentan un ingreso familiar per cápita nulo. Es el logaritmo de la razón entre los promedios aritméticos y geométricos del ingreso familiar per cápita de los individuos. El índice es nulo cuando no hay desigualdad y tiende al infinito a medida que la desigualdad aumenta al máximo. 
    </div>
		

	<div id="text_met">
		<span><b>Índice de Gini</b></span><br>
		Mide el nivel de desigualdad existente en la distribución de individuos según el ingreso familiar per cápita. Su valor varía entre 0, cuando no hay desigualdad (el ingreso familiar per cápita de todos los individuos tiene el mismo valor), y 1, cuando la desigualdad alcanza su nivel máximo (apenas un individuo percibe todos los ingresos). El universo de individuos se limita a las personas que viven en domicilios particulares permanentes.
    </div>

<div class="block-01-div-subtitle">TRABAJO<br /><br /></div>

	<div id="text_met">
		<span><b>Tasa de actividad –10 años o más</b></span><br>
		Razón entre las personas de al menos 10 años que eran económicamente activas, es decir, que estaban trabajando o desocupadas durante la semana de referencia del censo, y el total de personas en ese rango de edad multiplicada por 100. Se consideran desocupadas las personas que no estaban trabajando durante la semana de referencia pero habían buscado trabajo durante el mes anterior a la encuesta. 
    </div>
	
	<div id="text_met">
		<span><b>Tasa de desempleo –10 años o más</b></span><br>
		Porcentaje de la población económicamente activa (PEA) en ese rango de edades que se encontraba desempleada, es decir, que se encontraba parada en la semana anterior a la fecha del censo pero había buscado trabajo durante el mes anterior a la encuesta. 
    </div>
	
	<div id="text_met">
		<span><b>Tasa de empleo formal –18 años o más</b></span><br>
		Razón entre el número de personas de al menos 18 años con un trabajo formal y el número total de personas que trabajan en ese rango de edades multiplicada por 100. Se consideran formalmente ocupados los empleados con cédula de trabajo firmada, los militares del ejército, la marina, la aeronáutica, la policía militar o el cuerpo de bomberos, los funcionarios públicos y los empleadores y trabajadores independientes que cotizaban en un instituto de previsión social oficial. 
    </div>

<div class="block-01-div-subtitle">VIVIENDA<br /><br /></div>

	<div id="text_met">
		<span><b>Porcentaje de la población que vive en domicilios con agua canalizada</b></span><br>
		Razón entre la población que vive en un domicilio particular permanente con agua canalizada para una o más habitaciones y la población total residente en domicilios particulares permanentes multiplicada por 100. El agua puede provenir de una red, de un pozo, de un manantial o de un estanque abastecido por aguas-lluvia o un camión aljibe. 
    </div>
	
	<div id="text_met">
		<span><b>Porcentaje de la población que vive en domicilios con cuarto de baño y agua canalizada</b></span><br>
		Razón entre la población que vive en un domicilio particular permanente con agua canalizada en al menos una habitación y con cuarto de baño exclusivo y la población total residente en domicilios particulares permanentes multiplicada por 100. El agua puede provenir de una red, de un pozo, de un manantial o de un estanque abastecido por aguas-lluvia o un camión aljibe. El cuarto de baño exclusivo es aquel que dispone de una ducha o bañera y un inodoro. 
    </div>
	
	<div id="text_met">
		<span><b>Porcentaje de la población que vive en domicilios con densidad > 2</b></span><br>
		Porcentaje de la población que vive en un domicilio con una densidad superior a 2 personas por dormitorio. Razón entre la población que vive en un domicilio particular permanente con una densidad superior a 2 y la población total residente en domicilios particulares permanentes multiplicada por 100. La densidad del domicilio es la razón entre el número total de habitantes del domicilio y el número total de habitaciones usadas como dormitorio. 
    </div>
	
	<div id="text_met">
		<span><b>Porcentaje de la población que vive en domicilios urbanos que cuentan con servicios de recolección de basura</b></span><br>
		Razón entre la población que vive en un domicilio que cuenta con servicios de recolección de basura y la población total residente en domicilios particulares permanentes multiplicada por 100. Se incluyen las situaciones en que la recolección de basura es realizada directamente por una empresa pública o privada o bien se deposita la basura en un recipiente, tanque o depósito fuera del domicilio para su posterior recolección por la empresa prestadora de servicios. Se consideran únicamente los domicilios particulares permanentes ubicados en áreas urbanas. 
    </div>
	
	<div id="text_met">
		<span><b>Porcentaje de la población que vive en domicilios con suministro eléctrico</b></span><br>
		Razón entre la población que vive en un domicilio particular permanente con suministro eléctrico y la población total residente en domicilios particulares permanentes multiplicada por 100. Se considera todo suministro eléctrico proveniente de una red interconectada o no, con o sin medidor.
    </div>

<div class="block-01-div-subtitle">VULNERABILIDAD<br /><br /></div>

	<div id="text_met">
		<span><b>Porcentaje de personas que viven en domicilios con servicios de suministro de agua y alcantarillado inadecuados</b></span><br>
		Razón entre las personas que viven en domicilios cuyo suministro de agua no proviene de una red y cuyas aguas servidas no son recolectadas por una red de alcantarillado o una fosa séptica y la población total residente en domicilios particulares permanentes multiplicada por 100. Se consideran únicamente los domicilios particulares permanentes. 
    </div>
	
	<div id="text_met">
		<span><b>Porcentaje de personas que viven en domicilios con paredes inadecuadas</b></span><br>
		Razón de la población que vive en domicilios cuyas paredes no son de albañilería ni de madera desbastada y la población total residente en domicilios particulares permanentes multiplicada por 100. Se consideran únicamente los domicilios particulares permanentes.
    </div>
	
	<div id="text_met">
		<span><b>Porcentaje de personas que viven en domicilios vulnerables a la pobreza y de ancianos</b></span><br>
		Razón entre las personas que viven en domicilios vulnerables a la pobreza (con ingreso per cápita inferior a medio sueldo mínimo de agosto de 2010) y donde la principal fuente de ingresos proviene de habitantes con al menos 65 años y la población total residente en domicilios particulares permanentes multiplicada por 100.
    </div>

<div class="block-01-div-subtitle">POBLACIÓN<br /><br /></div>

	<div id="text_met">
		<span><b>Población total en domicilios particulares permanentes</b></span><br>
		Población que vive en domicilios particulares permanentes. Este concepto no incluye a las personas que viven en domicilios colectivos, tales como pensiones, hoteles, cárceles, cuarteles y hospitales. 
    </div>
	
	<div id="text_met">
		<span><b>PEA –10 años o más</b></span><br>
		Población económicamente activa. Este concepto corresponde al número de personas en ese rango de edades que, durante la semana de referencia del censo, se encontraban ocupadas en el mercado de trabajo o estaban desocupadas pero habían buscado trabajo durante el mes anterior a la fecha de la encuesta.
    </div>
	
<div class="block-01-div-subtitle">DESARROLLO HUMANO<br /><br /></div>

	<div id="text_met">
		<span><b>IDHM - Índice de Desarrollo Humano Municipal</b></span><br>
		Promedio geométrico de los índices que miden las dimensiones ingresos, educación y longevidad con el mismo coeficiente. 
    </div>
	
	<div id="text_met">
		<span><b>IDHM Ingreso (Índice de Desarrollo Humano Municipal – dimensión ingresos)</b></span><br>
		Índice de la dimensión ingresos, que constituye uno de los tres componentes del IDHM. Se obtiene a partir del indicador de ingresos per cápita mediante la fórmula siguiente: (ln [valor observado del indicador] - ln [valor mínimo]) / (ln [valor máximo] - ln [valor mínimo]), donde los valores mínimo y máximo son 8,00 R$ y 4033,00 R$ (precios de agosto de 2010).
    </div>
	
	<div id="text_met">
		<span><b>IDHM Longevidad (Índice de Desarrollo Humano Municipal – dimensión longevidad)</b></span><br>
		Índice de la dimensión longevidad, que constituye uno de los tres componentes del IDHM. Se obtiene a partir del indicador de esperanza de vida al nacimiento mediante la siguiente fórmula: ([valor observado del indicador] – [valor mínimo]) / ([valor máximo] – [valor mínimo]), donde los valores mínimo y máximo son 25 y 85 años, respectivamente. 
    </div>
	
	<div id="text_met">
		<span><b>IDHM Educación (Índice de Desarrollo Humano Municipal – dimensión educación)</b></span><br>
		Índice sintético de la dimensión educación, que constituye uno de los tres componentes del IDHM. Se obtiene calculando el promedio geométrico del subíndice de asistencia de niños y jóvenes al sistema escolar, con un coeficiente de 2/3, y del subíndice del nivel de educación de la población adulta, con un coeficiente de 1/3. 
    </div>
	
	<div id="text_met">
		<span><b>Subíndice del nivel de educación primaria y secundaria de la población adulta - IDHM Educación</b></span><br>
		Subíndice seleccionado para elaborar el IDHM Educación que refleja el nivel de educación de la población adulta. Se obtiene calculando el porcentaje de jóvenes y adultos de al menos 18 años que completaron la educación primaria y secundaria. 
    </div>
	
	<div id="text_met">
		<span><b>Subíndice de asistencia escolar de la población joven - IDHM Educación</b></span><br>
		Subíndice seleccionado para elaborar el IDHM Educación que refleja la asistencia de los niños y jóvenes a establecimientos educativos adecuados para su edad. Se obtiene calculando el promedio aritmético simple de 4 indicadores: el porcentaje de niños de 5 a 6 años que van a la escuela, el porcentaje de niños de 11 a 13 años que cursan el 2º ciclo de la educación primaria, el porcentaje de jóvenes de 15 a 17 años que completaron la educación primaria y el porcentaje de jóvenes de 18 a 20 años que completaron la educación secundaria.
    </div>

