        <div id="text_met">
	<span><b>Limitaciones</b></span><br />
	La gran limitación de este indicador es que no toma en cuenta la desigualdad de los ingresos entre los habitantes de cada municipio. Así, un municipio puede registrar un ingreso per cápita elevado  y, al mismo tiempo, tener una gran proporción de la población viviendo en situación de pobreza.
        </div>
