        <div id="text_met">
	<span><b>¿Cómo se calcula el IDHM Ingreso?</b></span><br />
	Para calcular el IDHM Ingreso se utiliza la siguiente fórmula:<br />
            IDHM-I = (ln [ingreso per cápita municipal] - ln [valor mínimo de referencia]) / (ln [valor máximo de referencia] - ln [valor mínimo de referencia])<br /><br />
            <p>La aplicación del logaritmo en la fórmula acerca los valores superiores de ingresos per cápita a los valores inferiores y, de esa forma, reduce la desigualdad de ingresos existente. No obstante, ese procedimiento considera que, a medida que los ingresos per cápita aumentan, el retorno de dicho aumento disminuye en términos de desarrollo humano.</p>
            <p>Máximo: 4033,00 R$ – corresponde al valor del menor ingreso per cápita del 10 % más rico residente en la unidad federativa con mayor ingreso promedio del país en el período analizado, el Distrito Federal.</p>
            <p>Mínimo: 8,00 R$ – corresponde aproximadamente a 100 US$ PPA, el límite adoptado para calcular el IDH Global.</p>
            <p>Para un municipio con ingreso municipal per cápita de 827,35 R$, el cálculo sería el siguiente:<br /> 
            IDHM-I = (ln 827,35 - ln 8,00) / (ln 4033,00 - ln 8,00) => IDHM-I = 0,745</p>
