	<span><b>Comparación y ajustes a partir del IDH Global</b></span><br />
	El producto interior bruto (PIB) es el valor agregado de la producción de todos los bienes y servicios de un territorio determinado durante un año. El PIB per cápita, que se obtiene dividiendo dicho valor por la población del país, fue el indicador usado por el PNUD a nivel mundial para calcular el IDH de los países y Estados hasta 2010. En 2010, este indicador fue sustituido por el Ingreso Nacional Bruto (INB) per cápita.
            <p>El PIB es inadecuado para medir los ingresos de los habitantes de un municipio, pues no todos los ingresos de la producción generada dentro del municipio son recaudados por sus habitantes (y viceversa). Por otro lado, no hay estadísticas municipales para la INB per cápita. La alternativa adoptada es el cálculo del ingreso municipal per cápita a partir de los datos del censo demográfico.</p>
        </div>
