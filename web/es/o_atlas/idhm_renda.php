	<span><b>Indicadores de apoyo para el análisis del IDHM Ingreso</b></span><br />
	Además del ingreso per cápita municipal, el Atlas incluye, para servir de apoyo al análisis de esta dimensión, distintos indicadores de desigualdad de los ingresos (ingreso per cápita por quintil de la población, razón entre el ingreso per cápita del 10 % y el 20 % más rico y el ingreso per cápita del 40 % más pobre, índices de Gini y de Theil) y de pobreza (considerando 3 niveles de pobreza: 70 R$, 140 R$ y 255 R$). El cálculo de estos indicadores se basa en el ingreso familiar per cápita, presuponiendo que todas las personas de un hogar tienen los mismos ingresos.
        </div>
