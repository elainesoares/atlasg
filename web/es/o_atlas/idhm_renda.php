	<span><b>¿Qué compone el IDHM Ingreso?</b></span><br />
	El IDHM Ingreso considera el ingreso municipal per cápita, es decir, el ingreso promedio mensual de las personas que habitan un municipio determinado expresado en reales del 1 de agosto de 2010.
        </div>
        <div class="clear"></div>
        <img src="./img/o_atlas/o_que_merece.jpg" id="dim_saud_images">
        <div id="text_met">
	<span><b>¿Qué mide este indicador?</b></span><br />
	El ingreso per cápita mide la capacidad promedio de adquisición de bienes y servicios por parte de los habitantes del municipio.
        </div>
        <div class="clear"></div>
        <img src="./img/o_atlas/porque_esse_indicador.jpg" id="dim_saud_images">
        <div id="text_met">
	<span><b>¿Por qué este indicador?</b></span><br />
	Este indicador refleja la capacidad de los habitantes de un municipio para garantizar un estándar de vida que les permita satisfacer necesidades básicas como el agua, la alimentación y la vivienda.
        </div>
        <div class="clear"></div>
        <img src="./img/o_atlas/como_e_calculado.jpg" id="dim_saud_images">
        <div id="text_met">
	<span><b>¿Cómo se calcula este indicador?</b></span><br />
	Los valores se obtienen a partir de las respuestas proporcionadas en el cuestionario del censo demográfico. Este indicador corresponde a la razón entre la suma de todos los ingresos de los individuos que habitan el municipio percibidos el mes anterior a la fecha del censo y el número total de esos individuos. Los valores de los ingresos contabilizados a partir de los censos demográficos de 1991 y 2000, expresados en cruzeiros del 1 de septiembre de 1991 y en reales del 1 de agosto de 2000, fueron convertidos en reales constantes del 1 de agosto de 2010 (fecha de referencia del censo de 2010). Para ello, se utilizó la serie del Índice Nacional de Precios al Consumidor (INPC) del IBGE (convertida en una serie basada en el primer día de cada mes), pero, considerando que dicha serie subestimó en un 22,25 % la inflación de julio de 1994, se aplicó un factor corrector de 1,2225 a partir de esa fecha.
        </div>
        <div class="clear"></div>
