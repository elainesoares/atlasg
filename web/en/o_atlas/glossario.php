<div class="areatitle">Glossary</div>
<div class="block-01-div-subtitle">DEMOGRAPHY AND HEALTH<br /><br /></div>
	<div id="text_met">
		<span><b>Life expectancy at birth</b></span><br>
		Average number of years that people would live from birth if prevailing mortality level and standard (by age) of the year of the Census were to remain constant throughout the life.
    </div>
	
	<div id="text_met">
		<span><b>Total fertility rate</b></span><br>
		Average number of children a woman will have during her reproductive age (15-49 years old).
    </div>
	
	<div id="text_met">
		<span><b>Infant mortality</b></span><br>
		Number of the children that will not survive the first year of life, per 1000 live births.
    </div>
	
	<div id="text_met">
		<span><b>Mortality up to 5 years of age</b></span><br>
		Probability of dying between birth and 5 years of age, per 1000 live births.
    </div>
	
	<div id="text_met">
		<span><b>Survival probability up to 40 years of age</b></span><br>
		Probability of a newborn child to live up to 40 years of age prevailing mortality level and standard (by age) of the year of the Census were to remain constant throughout the life.
    </div>
	
	<div id="text_met">
		<span><b>% of the people living in households with a dependency ratio > 75%</b></span><br>
		The ratio of people living in households in which the dependency ratio is greater than 75% and the number of people living in permanent private households multiplied by 100. The dependency ratio is measured by the ratio between the number of people aged 14 or less and 65 years or older (the dependent population) and the number of people aged 15 to 64 years of age (potentially-active population) multiplied by 100.
    </div>
	
	<div id="text_met">
		<span><b>Aging rate</b></span><br>
		Ratio of the population aged 65 or older and the total population multiplied by 100.
    </div>


<div class="block-01-div-subtitle">EDUCATION<br /><br /></div>
	<div id="text_met">
		<span><b>The expected years of schooling</b></span><br>
		The average number of years of education that a generation of children entering into the education system must complete by the age of 18 years, if the current standards are maintained throughout his/her school life.
    </div>
	
	<div id="text_met">
		<span><b>Illiteracy rate of the population aged 11 to 14 years</b></span><br>
		The ratio of the population aged 11 to 14 years who cannot read and write simple notes and the total number of population in this age group multiplied by 100.
    </div>
	
	<div id="text_met">
		<span><b>The gross attendance frequency in basic education</b></span><br>
		The ratio between the total number of people of any age attending a basic education institution (primary or secondary school) and the population aged 6 to 17 years, multiplied by 100.
    </div>
	
	<div id="text_met">
		<span><b>The net attendance frequency in basic education</b></span><br>
		The ratio between the number of people aged 6 to 17 years attending a basic education institution (primary or secondary school) and the total population of the same age group, multiplied by 100.
    </div>

<div class="block-01-div-subtitle">INCOME<br /><br /></div>
	<div id="text_met">
		<span><b>Income per capita</b></span><br>
		The ratio between the sum of the income of all individuals living in permanent private households and the total number of these individuals. Values are expressed in Brazilian reals (rate of 1st August 2010).
    </div>
	
	<div id="text_met">
		<span><b>The share of the extremely poor</b></span><br>
		Share of individuals with household monthly per capita incomes equal to or less than R$ 70.00 - in the rate of Brazilian reals set in August 2010. This share is limited to individuals who live in permanent private households.
    </div>
	
	<div id="text_met">
		<span><b>The share of the poor</b></span><br>
		Share of individuals with household monthly per capita income equal to or less than R$ 140.00 - in the rate of Brazilian reals set in August 2010. This share is limited to individuals who live in permanent private households.
    </div>
	
	<div id="text_met">
		<span><b>Share of the people vulnerable to poverty</b></span><br>
		The share of individuals with household per capita incomes equal to or less than R$ 255.00 per month (in the rate of Brazilian reals set in August 2010), equivalent to half the minimum wage at the time. This share is limited to individuals who live in permanent private households.
    </div>
	
	<div id="text_met">
		<span><b>Ratio of the richest 20% / the poorest 40%</b></span><br>
		This ratio measures the degree of inequality in the distribution of individuals, according to household per capita income. This compares the average per capita income of individuals in the richest quintile of the population with the average per capita income of individuals in the poorest two quintiles. This measurement is limited to individuals who live in permanent private households.
    </div>
	
	<div id="text_met">
		<span><b>The Theil Index - L</b></span><br>
		The Theil Index measures inequality in the distribution of individuals according to the per capita income, excluding those whose household income per capita is zero. It is calculated by a logarithm of the ratio between the arithmetic and geometric averages of the per capita household incomes of individuals: the index is zero when income inequality does not exist and tends to infinity when inequality tends to the maximum.
    </div>
		

	<div id="text_met">
		<span><b>The Gini index</b></span><br>
		The Gini Index measures the degree of inequality in the distribution of individuals according to income per capita. Its value ranges from 0, when there is no inequality (the per capita household income of all individuals have same value) to 1, when inequality is at its maximum (only one person holds all the income). This measurement is limited to individuals who live in permanent private households.
    </div>

<div class="block-01-div-subtitle">LABOUR<br /><br /></div>

	<div id="text_met">
		<span><b>Activity rate – population aged 10 years or older</b></span><br>
		The ratio of people aged 10 years or older who are economically active - in other words, people who are employed or unemployed in the reference week of the Census and the total number of people in this age group, multiplied by 100. A person was considered unemployed when not employed during the reference week and having sought work in the month prior to the study.
    </div>
	
	<div id="text_met">
		<span><b>Unemployment rate – population aged 10 years or older</b></span><br>
		The unemployment rate corresponds to the percentage of the economically-active population (EAP) in the age group that was unemployed, that is, not busy in the week before the Census, but having sought work during the month prior to the study.
    </div>
	
	<div id="text_met">
		<span><b>Degree of formal employment – population aged 18 years or older</b></span><br>
		The ratio between the number of people aged 18 years or older who are formally employed and the total number of employed persons in this age group, multiplied by 100. People were considered formally occupied employees when they have a formal contract - the military, army, navy aircraft, military police or fire department, people employed by the legal regime of public officials, as well as employers and the self-employed who contribute to official social security institutes.
    </div>

<div class="block-01-div-subtitle">HOUSING<br /><br /></div>

	<div id="text_met">
		<span><b>Percentage of the population living in households with piped water</b></span><br>
		The ratio of the population living in permanent private households with piped water to one or more rooms in the total population living in permanent private households, multiplied by 100. The water may be derived from general network, wells, springs or reservoirs fed by rainwater or tanker car.
    </div>
	
	<div id="text_met">
		<span><b>Percentage of the population living in households with a toilet and running water</b></span><br>
		The ratio of the population living in permanent private households with piped water in at least one of their rooms and an exclusive bathroom and the total population living in permanent private households, multiplied by 100. The water may be derived from general network, wells, springs or reservoirs fed by rainwater or tanker car. An exclusive bathroom is defined as a room that has a shower or a bath and a toilet unit.
    </div>
	
	<div id="text_met">
		<span><b>Percentage of households with population density > 2</b></span><br>
		Percentage of population living in households with a population density higher than 2 people per bedroom. 
		The ratio of the population living in permanent private households with density greater than 2 and the total population living in permanent private households, multiplied by 100. The household density is given by the ratio between the total number of people in the household and the total number of rooms used as dormitories.
    </div>
	
	<div id="text_met">
		<span><b>Percentage of the population living in urban households with garbage collection services</b></span><br>
		The ratio of the population living in households with garbage collection and the total population living in permanent private households, multiplied by 100. This includes situations where garbage collection is performed directly by public or private companies, or garbage is deposited outside the household for later collection by the service provider. Only permanent households located in urban areas are considered.
    </div>
	
	<div id="text_met">
		<span><b>Percentage of the population living in households with electricity</b></span><br>
		The ratio of the population living in permanent private households with electricity and the total population living in permanent private households, multiplied by 100. Power, regardless of whether it is derived from the general network or not, and with or without gauges, is taken into account.
    </div>

<div class="block-01-div-subtitle">VULNERABILITY<br /><br /></div>

	<div id="text_met">
		<span><b>% of people in households with inadequate water supply and sanitation</b></span><br>
		The ratio of people living in households without water supply provided by the general network and without sanitation carried out by a general collection system or a septic tank and the total population living in permanent private households, multiplied by 100. Only permanent households are taken into account.
    </div>
	
	<div id="text_met">
		<span><b>% of people in households with inadequate walls</b></span><br>
		The ratio of people living in households whose walls are not made of masonry or plywood and the total population living in permanent private households, multiplied by 100. Only permanent households are taken into account.
    </div>
	
	<div id="text_met">
		<span><b>% of people in households vulnerable to poverty and dependent on the elderly</b></span><br>
		Ratio of people living in households that are vulnerable to poverty (with per capita income less than half the minimum wage in August 2010) and in which the main source of income comes from residents aged 65 years or older and the total resident population in permanent private households, multiplied by 100.
    </div>

<div class="block-01-div-subtitle">POPULATION<br /><br /></div>

	<div id="text_met">
		<span><b>Total population in permanent private households </b></span><br>
		Population living in permanent private households. Excluding residents in collective households, such as pensions, hotels, prisons, barracks, hospitals.
    </div>
	
	<div id="text_met">
		<span><b>EAP – aged 10 years or older</b></span><br>
		Economically-active population. It corresponds to the number of people in this age group that were (in the reference week of the census) employed in the labour market or unemployed people having sought work in the month prior to the date of the survey.
    </div>
	
<div class="block-01-div-subtitle">HUMAN DEVELOPMENT<br /><br /></div>

	<div id="text_met">
		<span><b>MHDI - Municipal Human Development Index</b></span><br>
		Municipal Human Development Index. Geometric mean of the dimensions pertaining to the indices of income, education and longevity, calculated with equal weights.
    </div>
	
	<div id="text_met">
		<span><b>MHDI Income (Municipal Human Development Index – Income Dimension)</b></span><br>
		The index of the Income dimension is one of the three components of the MHDI. It is obtained by the Income per capita indicator using the following formula: [ln (observed value of the indicator) - ln (minimum)] / [ln (maximum) - ln (minimum)] where the minimum and maximum values are R$ 8.00 and R$ 4,033.00 (prices of August 2010).
    </div>
	
	<div id="text_met">
		<span><b>MHDI Longevity (Municipal Human Development Index - Longevity Dimension)</b></span><br>
		The index of the Longevity dimension is one of three components of the MHDI. It is obtained by the Life expectancy at birth indicator, using the following formula: [(observed value of the indicator) - (minimum)] / [(maximum) - (minimum)]m where the minimum and maximum values are, respectively, 25 and 85 years.
    </div>
	
	<div id="text_met">
		<span><b>MHDI Education (Municipal Human Development Index - Education Dimension)</b></span><br>
		The synthetic index of the Education dimension is one of three components of the MHDI. It is obtained through the geometric mean of the sub-index of school attendance of children and young people (with a weight of 2/3) and the sub-index of the educational level of the adult population (with a weight of 1/3).
    </div>
	
	<div id="text_met">
		<span><b>Primary education sub-index of the adult population – MHDI Education</b></span><br>
		The selected sub-index of MHDI Education representing the educational level of the adult population. It is obtained by the % indicator of young adults aged 18 or older having completed primary education.
    </div>
	
	<div id="text_met">
		<span><b>School attendance sub-index for young people – MHDI Education</b></span><br>
		The selected sub-index of the MHDI Education representing the school attendance frequency of children and young people in grades that are appropriate to their age. It is obtained by the simple arithmetic mean of four indicators: % of children aged 5 to 6 years attending school, % of children aged 11 to 13 years attending the final years of primary education, % of young people aged 15 to 17 years having completed primary education and % young people aged 18 to 20 years having completed secondary education.
    </div>

