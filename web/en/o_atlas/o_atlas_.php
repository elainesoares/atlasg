    <p>The <b>Atlas of Human Development in Brazil 2013</b> is a consultation platform for the Municipal Human Development Index – MHDI – of the 5,565 Brazilian municipalities and for over 180 indicators of population, education, housing, health, work, income and vulnerability, with data extracted from the Demographic Censuses of 1991, 2000 and 2010. </p>
    <p><b>Atlas Brazil 2013</b> is designed as a simple and user-friendly tool that facilitates access to information and the use of data, encouraging further analyses. It is a tool that provides an overview of the human development of municipalities and the inequality between them, in reference to various aspects of overall well-being. It is relevant through its ability to provide information about the political unit closest to the daily lives of citizens – the municipality.</p>
    <p>The municipalities are important parts of a complex mosaic and face numerous challenges, but also tremendous opportunities. There are over 5000 territories ripe with creativity and experience and searching for innovative local development solutions. By providing a closer look at the municipalities, Atlas Brazil 2013 leads the way and allows us to reflect upon the course of human development in the country.</p>
