        <div class="areatitle"> O que é Desenvolvimento Humano </div>
        <div class="areaTexto">
            <p>O conceito de desenvolvimento humano nasceu definido como um processo de ampliação das escolhas das pessoas para que elas tenham capacidades e oportunidades 
                para serem aquilo que desejam ser.
            </p>
            <p>Diferentemente da perspectiva do crescimento econômico, que vê o bem-estar de uma sociedade apenas 
                pelos recursos ou pela renda que ela pode gerar, a abordagem de desenvolvimento humano procura olhar 
                diretamente para as pessoas, suas oportunidades e capacidades. A renda é importante, mas como um dos 
                meios do desenvolvimento e não como seu fim. É uma mudança de perspectiva: com o desenvolvimento humano, 
                o foco é transferido do crescimento econômico, ou da renda, para o ser humano.
            </p>
            <p>O conceito de Desenvolvimento Humano também parte do pressuposto de que para aferir o avanço na qualidade 
                de vida de uma população é preciso ir além do viés puramente econômico e considerar outras características 
                sociais, culturais e políticas que influenciam a qualidade da vida humana. Esse conceito é a base do Índice 
                de Desenvolvimento Humano (IDH) e do Relatório de Desenvolvimento Humano (RDH), publicados anualmente pelo PNUD.
            </p>
