	<span><b>Support indicators in analysing MHDI Income</b></span><br />
	In addition to municipal income per capita, Atlas Brazil 2013 presents various supportive indicators (such as income <i>per capita</i> by population quintiles, the ratio between the income <i>per capita</i> of the richest 10% and 20% and the income <i>per capita</i> of the poorest 40% of the population, as well as the Gini and Theil Indices) that examine income-related inequalities. Poverty is also reviewed by three poverty lines - R$ 70, R$ 140 and R$ 255. To calculate these indicators, Atlas 2013 has adopted the household income <i>per capita</i> (assuming that all people in the same household have the same income).
        </div>
