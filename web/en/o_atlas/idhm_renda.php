	<span><b>Comparison and adjustments within the global HDI</b></span><br />
	The Gross Domestic Product (GDP) is the value added by the production of all goods and services over a year within a given boundary. The GDP <i>per capita</i> is the division of this value by the population of a country that the UNDP has used for the calculation of HDI of countries and states worldwide until 2010. In 2010, this indicator was replaced by the GNI (Gross National Income) <i>per capita</i>. 
            <p>The use of GDP <i>per capita</i> becomes inadequate when assessing the income of the inhabitants of a municipality, because not all of the income generated by production within the municipal area is appropriated for the resident population (and vice versa). On the other hand, there are no municipal statistics for GNI <i>per capita</i>. The alternative used calculates the municipal income <i>per capita</i> obtained from the Demographic Census.</p>
        </div>
        <div class="clear"></div>
