	<span><b>How to calculate MHDI Income</b></span><br />
	The formula applied to calculate MHDI Income is:<br />
            MHDI-I = [ln (municipal income <i>per capita</i>) - ln (minimum value of reference)] / [ln (maximum value of reference) - ln (minimum value of reference)]<br /><br />
            <p>This logarithm applied in the formula brings the maximum values of income <i>per capita</i> closer to the minimum values â€“ and therefore, it reduces the existing income inequality. However, this procedure assumes that, as the income <i>per capita</i> increases, the return of this increased income, in terms of human development, decreases.</p>
            <p>Maximum amount: R$ 4,033.00 - is equivalent to the amount of the lowest income <i>per capita</i> among the richest 10% of residents at FUs (Federative Units) with the highest average income in the country during the analysed period: the Federal District.</p>
            <p>Minimum amount: R$ 8.00 - is equivalent to, approximately, US$ 100 PPS (Purchasing Power Standards), which is the constraint used in calculating the global HDI. </p>
            <p>For a municipality with a municipal income <i>per capita</i> of R$ 827.35, the calculation would be as follows: <br /> 
            MHDI-I = (ln 827.35 - 8.00 ln) / (ln 4033.00 - 8.00 ln) => MHDI-I = 0.745.</p>
