        <div id="text_met">
	<span><b>Limitations</b></span><br />
	The major limitation of this indicator is that it does not examine income inequality among the inhabitants of the municipality. Hence, a municipality may have a high income per capita, but, at the same time, a large portion of its population may be living in poverty.
        </div>
        <div class="clear"></div>
        <img src="./img/o_atlas/peso_variaveis.jpg" id="dim_saud_images">
