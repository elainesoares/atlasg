	<span><b>What does the MHDI Income consist of?</b></span><br />
	The MHDI Income examines the municipal income <i>per capita</i>; in other words, the monthly income of the residents in a certain municipality, in Brazilian Reals (R$.) from 1st of August 2010.
        </div>
        <div class="clear"></div>
        <img src="./img/o_atlas/o_que_merece.jpg" id="dim_saud_images">
        <div id="text_met">
	<span><b>What does the indicator measure?</b></span><br />
	Income <i>per capita</i> measures the average capacity of procuring goods and services by the inhabitants of a municipality.
        </div>
        <div class="clear"></div>
        <img src="./img/o_atlas/porque_esse_indicador.jpg" id="dim_saud_images">
        <div id="text_met">
	<span><b>Why this indicator?</b></span><br />
	This indicator shows the ability of the inhabitants in a municipality to ensure a decent standard of living to ensure their basic needs, such as water, food and shelter.
        </div>
        <div class="clear"></div>
        <img src="./img/o_atlas/como_e_calculado.jpg" id="dim_saud_images">
        <div id="text_met">
	<span><b>How is the indicator calculated?</b></span><br />
	The values are obtained from the questionnaire responses of a sample of the Demographic Census. The indicator presents the ratio of the sum of all income that all of the individuals of the municipality received in the month prior to the date of the Census, and the total number of these individuals. The income figures calculated from the Censuses of 1991 and 2000, in cruzeiros of 1st September 1991 and in reais, of 1st August 2000, were converted into reais from August 1, 2010 (the reference date of the 2010 Census.) For this reason, we used the series from the National Index Consumer Price Index (INPC) from IBGE (converted into a series centered on the first day of each month) but, considering that this series underestimated inflation by 22.25% in July/1994 from that date forward, a correcting factor of 1.2225 was applied.
        </div>
        <div class="clear"></div>
