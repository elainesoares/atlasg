    <div class="areatitle" style="margin-top: 88px;">How the MHDI is calculated?</div>
    <div id="calculo_idhm">
        <div id="text_calculo_idhm">
            <p>The goal of the construction of the methodology of calculating the MHDI is to adapt the methodology of the global HDI:</p><br />
            <div id="a_b" style='margin-right: 30px;'>
                <b><font color="#BDBDBD" size="5">a)</font></b><br />
                a)	To adjust the methodology into the Brazilian context, seeking the most suitable indicators to evaluate the conditions of the smallest sociopolitical units - the municipalities.
            </div>
            <div id="a_b">
                <b><font color="#BDBDBD" size="5">b)</font></b><br />
                b)	To adapt the global HDI methodology to the available indicators from the Brazilian Demographic Censuses to ensure same data sources and comparability with all municipalities.
            </div>
            <div class="clear"></div>
            
            <br />
            <p><b>Long and healthy life </b> is measured by the life expectancy at birth and calculated by the indirect method and using data from the Demographic Census of The Brazilian Institute of Geography and Statistics (IBGE). This indicator shows the average number of years that a person born in a particular municipality live from birth maintaining the same mortality patterns.
            </p>
            
            <br />
            <p><b>Access to knowledge </b>is measured by two indicators. The level of education of the adult population is measured by the percentage of people aged 18 years or older having primary education completed – with a weight of 1. The educational flow of young people is measured by the arithmetic mean of the percentage of children aged 5 to 6 years attending school, the percentage of young people aged 11 to 13 years attending the final years of primary education, the percentage of young people aged 15 to 17 years having completed primary education and the percentage of young people aged 18 to 20 years having completed secondary education –weight of 2. The measurement follows the school-age population at four important moments of their educational lives. This makes it easier for managers to identify whether the children and young people are in suitable grades at certain ages. The MHDI Education is a result of the geometric mean of these two components. The used data is collected via demographic census by the Brazilian Institute of Geography and Statistics (IBGE).
            </p>
            
            <br />
            <p><b>Standard of living </b> is measured by the municipal income <i>per capita</i>; in other words, by the average income of a resident of a certain municipality. It is the sum of the income of all residents, divided by the number of people living in the municipality – including children and people who are registered without income. The applied data was collated by the Demographic Censuses conducted by the Brazilian Institute of Geography and Statistics (IBGE).</p>
        </div>
