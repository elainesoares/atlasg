    <div id="texto_idhm">
        <p>In 2012, UNDP Brazil, the Institute for Applied Economic Research (IPEA) and the João Pinheiro Foundation (FJP) undertook the challenge of adapting the global HDI methodology to calculate a municipal HDI (MHDI) of the 5,565 Brazilian municipalities, using data from Demographic Census 2010. Also, the MHDI of the years 1991 and 2000 was re-calculated with the adopted methodology, in order to match the municipal information of the years 1991, 2000 and 2010 – taking into account the administrative divisions in the period and allowing spatial and temporal comparison between the municipalities.<p/>
        <p>The Brazilian MHDI has the same three dimensions as the global HDI - longevity, education and income -, but MHDI goes further with matching the global methodology to the Brazilian context with available national indicators. Even though the indices measure the same phenomena, the indicators taken into account in MHDI are more suitable when evaluating the development of the Brazilian municipalities. </p> 
        <p>Thus, with its three components - MHDI Longevity, MHDI Education, MHDI Income – the MHDI tells a bit of the history of the Brazilian municipalities in three important dimensions of human development over the recent decades.</p>
        <p>The MHDI is accompanied by more than 180 socioeconomic indicators that support the MHDI analysis and extend the understanding of the phenomena and dynamics the municipal development is facing. The MHDI and the supportive indicators are brought together in the Atlas of Human Development in Brazil 2013.</p>
