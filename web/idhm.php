    <div id="texto_idhm">
        <p>Em 2012, o PNUD Brasil, o IPEA e a Fundação João Pinheiro assumiram o desafio de adaptar a metodologia do IDH global para calcular o IDH Municipal (IDHM) dos 5.565 municípios brasileiros a partir de dados do Censo Demográfico de 2010. Também recalculou-se o IDHM, a partir da metodologia adotada, para os anos de 1991 e 2000, por meio de uma minuciosa compatibilização das áreas municipais entre 1991, 2000 e 2010 para levar em conta as divisões administrativas ocorridas no período e  permitir a comparabilidade temporal e espacial entre os municípios. O desafio já havia sido enfrentado nas edições do Atlas de 1998 e 2003. <p/>
        <p>O IDHM  brasileiro segue as mesmas três dimensões do IDH Global – longevidade, educação e renda, mas vai além: adequa a metodologia global ao contexto brasileiro e à disponibilidade de indicadores nacionais. Embora meçam os mesmos fenômenos, os indicadores levados em conta no IDHM são mais adequados para avaliar o desenvolvimento dos municípios brasileiros. </p> 
        <p>Assim, o IDHM – incluindo seus três componentes, IDHM Longevidade, IDHM Educação e IDHM Renda - conta um pouco da história dos municípios em três importantes dimensões do desenvolvimento humano durante duas décadas da história brasileira.</p>
        <p>O IDHM é acompanhado por mais de 180 indicadores socioeconômicos, que dão suporte à análise do IDHM e ampliam a compreensão dos fenômenos e dinâmicas voltados ao desenvolvimento municipal. O IDHM e os indicadores de suporte ficam reunidos no Atlas do Desenvolvimento Humano no Brasil 2013.</p>
