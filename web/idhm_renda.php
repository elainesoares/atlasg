	<span><b>Comparação e ajustes a partir do IDH Global</b></span><br />
	O Produto Interno Bruto (PIB) é o valor agregado na produção de todos os bens e serviços ao longo de um ano dentro de determinada fronteira. O PIB <i>per capita</i> é a divisão desse valor pela população do país e foi o indicador usado pelo PNUD, mundialmente, para o cálculo do IDH dos países e dos estados até 2010. Em 2010, esse indicador foi substituído pela RNB (Renda Nacional Bruta) <i>per capita</i>.
            <p>Na avaliação da renda dos habitantes de um município, o uso do PIB per capita torna-se inadequado, pois, nem toda a renda gerada pela produção dentro da área do município é apropriada pela população residente (e vice-versa). Por outro lado, não há estatísticas municipais para a RNB <i>per capita</i>. A alternativa adotada é o cálculo da renda municipal <i>per capita</i>, auferida a partir do Censo Demográfico.</p>
        </div>
