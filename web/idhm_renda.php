	<span><b>Como é o cálculo do IDHM Renda</b></span><br />
	Para o cálculo do IDHM Renda, aplica-se a fórmula:<br />
            IDHM-R = [ln (renda <i>per capita</i> municipal) – ln (valor mínimo de referência)] / [ln(valor máximo de referência) – ln (valor mínimo de referência)]<br /><br />
            <p>A aplicação do logaritmo na fórmula aproxima os maiores valores de renda <i>per capita</i> dos menores e, com isso, reduz a desigualdade de renda existente. Mas esse procedimento considera que, à medida que a renda <i>per capita</i> se eleva, o retorno desse acréscimo de renda, em termos de desenvolvimento humano, diminui.</p>
            <p>Máximo:  R$4.033,00 - corresponde ao valor da menor renda <i>per capita</i> entre os 10% mais ricos residentes na UF com maior renda média do país no período analisado, o Distrito Federal.</p>
            <p>Mínimo: R$8,00 - corresponde a aproximadamente US$100 PPC, limite adotado para o cálculo do IDH Global.</p>
            <p>Para um município com renda municipal per capita de R$ 827,35, o cálculo ficaria assim:<br /> 
            IDHM-R = (ln 827,35 - ln 8,00) / (ln 4033,00- ln 8,00) => IDHMR = 0,745.</p>
