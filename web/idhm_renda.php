	<span><b>O que compõe o IDHM Renda</b></span><br />
	O IDHM Renda considera a renda municipal <i>per capita</i>, ou seja, a renda média mensal dos indivíduos residentes em determinado município, expressa em reais de 1º de agosto de 2010.
