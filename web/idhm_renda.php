	<span><b>Como é calculado o indicador?</b></span><br />
	Os valores são obtidos das respostas ao questionário da amostra do Censo Demográfico. O indicador corresponde à razão entre o somatório de todos os rendimentos de todos os indivíduos residentes no município, recebidos no mês anterior à data do Censo, e o número total desses indivíduos. Os valores dos rendimentos apurados a partir do Censo Demográfico de 1991 e 2000, em cruzeiros de 1º de setembro de 1991 e em reais de 1º de agosto de 2000, foram convertidos em reais constantes de 1º de agosto de 2010 (data de referência do Censo de 2010). Para isso, foi utilizada a série do Índice Nacional de Preços do Consumidor (INPC) do IBGE (convertida em uma série centrada no primeiro dia de cada mês), mas, considerando-se que essa série subestimou em 22,25% a inflação em julho/1994, aplicou-se, a partir dessa data, um fator corretor de 1,2225.
        </div>
