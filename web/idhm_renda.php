	<span><b>Indicadores de suporte à análise do IDHM Renda</b></span><br />
	Além da renda <i>per capita</i> municipal, o Atlas inclui, como suporte à análise dessa dimensão, diversos indicadores de desigualdade de renda (renda <i>per capita</i> por quinto da população, razão entre a renda <i>per capita</i> dos 10% e dos 20% mais ricos e a renda ,<i>per capita</i> dos 40% mais pobres, índices de Gini e de Theil) e de pobreza (considerando 3 linhas de pobreza - R$70, R$140 e R$255). Para o cálculo desses indicadores, adota-se a renda domiciliar <i>per capita</i>, tomando-se como pressuposto que, em um mesmo domicílio, todas as pessoas têm o mesmo rendimento.
        </div>
