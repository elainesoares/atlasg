	<span><b>Limitações</b></span><br />
	A grande limitação desse indicador é não considerar a desigualdade de renda entre os habitantes do município. Assim, um município pode apresentar uma elevada renda <i>per capita</i>, mas, ao mesmo tempo, pode ter uma grande parcela de sua população vivendo na pobreza.
